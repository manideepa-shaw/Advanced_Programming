public class Six {
   static void show(int num)
   {
    System.out.println(num);
   } 
   static void show(double num)
   {
    System.out.println(num);
   } 
   public static void main(String[] args) {
    int num1=5;
    short num2=9;
    double num3=10.5;
    System.out.print("Int : ");
    show(num1);
    System.out.print("Short : ");
    show(num2);
    System.out.print("Double : ");
    show(num3);
   }
}
